const STORAGE_KEY = 'meyos-lang';

function apply(lang) {
  if (!DICT[lang]) return;
  document.documentElement.setAttribute('lang', lang);
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = DICT[lang][key];
    if (val !== undefined) el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-attr]').forEach(el => {
    el.dataset.i18nAttr.split(',').forEach(p => {
      const [attr, key] = p.split(':').map(s => s.trim());
      const val = DICT[lang][key];
      if (val !== undefined) el.setAttribute(attr, val);
    });
  });
  try { localStorage.setItem(STORAGE_KEY, lang); } catch (_) {}
  document.querySelectorAll('.lang, .mobile-lang').forEach(g => {
    g.querySelectorAll('button').forEach(b => {
      b.classList.toggle('is-active', b.textContent.trim().toLowerCase() === lang);
    });
  });
  window.dispatchEvent(new CustomEvent('meyos:lang', { detail: { lang } }));
}

function initLang() {
  let saved = 'ru';
  try { saved = localStorage.getItem(STORAGE_KEY) || 'ru'; } catch (_) {}
  apply(saved);
}

window.MEYOS_I18N = { apply, get current() { try { return localStorage.getItem(STORAGE_KEY) || 'ru'; } catch (_) { return 'ru'; } } };

if (document.readyState === 'loading') document.addEventListener('DOMContentLoaded', initLang);
else initLang();
